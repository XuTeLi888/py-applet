# 定义答题程序
def quiz():
    answered = set()
    while len(answered) < 50:
        question_id = random.choice(list(set(range(1, 51)) - answered))
        question, correct_answer = questions[question_id]
        print(f"Q{question_id}: {question}")
        user_answer = input("正确/错误: ").strip().lower()
        if user_answer == ("v" if correct_answer == 'v' else "x"):
            print("回答正确！")
        else:
            print(f"回答错误！正确答案是：{'v' if correct_answer == 'v' else 'x'}")
        answered.add(question_id)

# 运行答题程序
quiz()
